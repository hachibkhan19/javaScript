
// Switch case
var char = prompt('Enter a character: ').toLowerCase();

switch(char){
    case 'a':        
        document.write('Vowel');
        break
    case 'e':        
        document.write('Vowel');
        break
    case 'i':        
        document.write('Vowel');
        break
    case 'o':        
        document.write('Vowel');
        break
    case 'u':        
        document.write('Vowel');
        break
    default:        
        document.write('Consonant');
}


